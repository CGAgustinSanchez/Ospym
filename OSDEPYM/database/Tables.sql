CREATE TABLE TESTPERSONA (
    ID					int				NOT NULL	IDENTITY	PRIMARY KEY,
	NOMBRE				varchar(255),
    APELLIDO			varchar(255)	NOT NULL,
    DIRECCION			varchar(255),
    CIUDAD				varchar(255),
	FECHANACIMIENTO		date,
	NROCLIENTE			int,
	VEGETARIANO			bit,
	CURSO				varchar(255),
	SEXO				varchar(50)
);

CREATE TABLE HIJOS (
    IDHIJO				int				NOT NULL	IDENTITY	PRIMARY KEY,
	IDPERSONA			int				NOT NULL	FOREIGN KEY REFERENCES TESTPERSONA(ID),
	NOMBRE				varchar(255),
    APELLIDO			varchar(255)	NOT NULL
);

CREATE TABLE PROFESORES (
    IDPROFESOR			int				NOT NULL	IDENTITY	PRIMARY KEY,
	NOMBRE				varchar(255)	NOT NULL,
    APELLIDO			varchar(255)	NOT NULL,
    DIRECCION			varchar(255),
    CIUDAD				varchar(255),
);

CREATE TABLE CURSOS (
    IDCURSO				int				NOT NULL	IDENTITY	PRIMARY KEY,
    CURSO				varchar(255)	NOT NULL,
);

CREATE TABLE PROFESORESCURSOS (
    IDPROFESOR			int		NOT NULL	FOREIGN KEY REFERENCES PROFESORES(IDPROFESOR),
    IDCURSO				int		NOT NULL	FOREIGN KEY REFERENCES CURSOS(IDCURSO),
	PRIMARY KEY (IDPROFESOR,IDCURSO)
);

CREATE TABLE MOTIVOS_CONTACTO (
    IDMOTIVO			int				NOT NULL	IDENTITY	PRIMARY KEY,
	ETIQUETA			varchar(255)	NOT NULL,
);

CREATE TABLE CATEG_CONTACTO (
    IDCATEG				int				NOT NULL	IDENTITY	PRIMARY KEY,
    ETIQUETA			varchar(255)	NOT NULL
);

CREATE TABLE MOTIVO_CATEGORIA (
	IDMOTCAT			int				NOT NULL	IDENTITY	PRIMARY KEY,
    IDMOTIVO			int				NOT NULL	FOREIGN KEY REFERENCES MOTIVOS_CONTACTO(IDMOTIVO),
    IDCATEG				int				NOT NULL	FOREIGN KEY REFERENCES CATEG_CONTACTO(IDCATEG),
	CORREO				varchar(255)	NOT NULL
);

CREATE SEQUENCE contacto_seq
  AS INT                  -- DATA TIPE
  START WITH 1            -- EMPIEZA EN 1
  INCREMENT BY 1          -- SE INCREMENTA DE A 1
  MINVALUE 1              -- VALOR MINIMO 1
  MAXVALUE 999999         -- VALOR MAXIMO 999999
  CYCLE;                  -- CUANDO LLEGA AL NUMERO MAXIMO SE REINICIA LA SEQUENCIA
  
  